        <div id="emergency-do-box">
            <div id="do-title">
                <img src="<?php echo get_template_directory_uri(); ?>/assets/images/do.png" alt="Do Image">
                <h3>Do's</h3>
            </div>
            <div id="do-list">
                <ul>
                    <li>Damage from water and bacteria growth can begin within hours. Call for professional help.</li>
                    <li>Remove as much water as possible by mopping and blotting.</li>
                    <li>Remove wet area rugs or other floor coverings not permanently attached. Do not attempt to start removing wall-to-wall carpet.</li>
                    <li>Lift draperies off the floor, loop through a coat hanger and place the hanger on the drapery rod.</li>
                    <li>Wipe furniture, prop up wet furniture cushions for even drying and place aluminum foil under furniture legs.</li>
                    <li>Move photos, painting and art objects to a safe, dry location.</li>
                    <li>Keep books on shelves. Pack them tightly to prevent wraping of pages until a restoration professional can begin this specialized drying.</li>
                    <li>Open drawers, closets and cabinet doors to enhance drying.</li>
                </ul>
            </div>
        </div>
        <div id="emergency-dont-box">
            <div id="dont-title">
                <img src="<?php echo get_template_directory_uri(); ?>/assets/images/dont.png" alt="Dont Image">
                <h3>Dont's</h3>
            </div>
            <div id="dont-list">
                <ul>
                    <li>Do not enter a room with standing water until electricity has been turned off.</li>
                    <li>Do not use a regular household vacuum to remove water.</li>
                    <li>Do not lift tacked-down carpet without professional help.</li>
                    <li>Do not use electrical appliances while on wet carpet or flooring.</li>
                    <li>Do not disturb visible mold.</li>
                    <li>Do not remove books from shelves.</li>
                </ul>
            </div>
        </div>
    </div>
